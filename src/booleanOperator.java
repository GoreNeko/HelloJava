public class booleanOperator {
    public static void main(String[] args){
        int i = 6;
        int j = 6;
        System.out.println(i == j);
        System.out.println(i != j);

        int a = 30;
        int b = 7;
        int c = 7;
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(b <= c);
        System.out.println(b >= c);
    }
}
